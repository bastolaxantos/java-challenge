package com.scopic.javachallenge.models;

import com.scopic.javachallenge.enums.PlayerPosition;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import javax.persistence.*;
import javax.validation.constraints.NotBlank;
import javax.validation.constraints.NotNull;
import java.io.Serializable;
import java.util.HashSet;
import java.util.Set;

@Entity
@AllArgsConstructor
@NoArgsConstructor
@Getter
@Setter
@Table(name = "players")
public class Player implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @NotBlank
    private String name;

    @Enumerated(value = EnumType.STRING)
    @NotNull
    private PlayerPosition position;

    @OneToMany(targetEntity = PlayerSkill.class, cascade = {CascadeType.ALL})
    private Set<PlayerSkill> playerSkills = new HashSet<>();
}
